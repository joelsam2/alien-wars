void update_display_delay(void) {

  if (!start_flag) {
    for (int i = 0; i < 100; i++) {
      updateDisplay();

      if ((LPC_GPIO0->PIN & (1 << 30))) {
        start_flag = true;
        i = 100;
        break;
      }
    }
  }
}

void startup_screen(void) {
  LPC_GPIO0->DIR &= ~(1 << 30);

  while (1) {
    memcpy(matrixbuff, startup_new, (1 * 32 * 64));
    update_display_delay();
    memcpy(matrixbuff, startup_new_two, (1 * 32 * 64));
    update_display_delay();
    if ((LPC_GPIO0->PIN & (1 << 30)) || (start_flag == true)) {
      while (gpio_get(0, 30))
        ;
      break;
    }
  }

  clearDisplay();
  updateDisplay();
  start_flag = false;

  for (int i = 0; i <= 7; i++) {
    for (int j = 0; j < 64; j++) {
      matrixbuff[i + 11][j] = are[i][j];
    }
  }

  update_display_delay();

  for (int i = 0; i <= 7; i++) {
    for (int j = 0; j < 64; j++) {
      matrixbuff[i + 11][j] = you[i][j];
    }
  }
  update_display_delay();

  for (int i = 0; i <= 7; i++) {
    for (int j = 0; j < 64; j++) {
      matrixbuff[i + 11][j] = ready[i][j];
    }
  }
  update_display_delay();
}
